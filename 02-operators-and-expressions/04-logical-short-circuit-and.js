var isLoggedIn = true;
isLoggedIn && console.log("Welcome!");

// In this code, the `&&` operator is used to check if `isLoggedIn` is true. If it is true, the second part of the expression (`console.log("Welcome!")`) will be executed
// If `isLoggedIn` were false, the second part would not be executed, and nothing would be logged to the console. This is a common pattern in JavaScript for conditionally executing code without needing an explicit `if` statement.


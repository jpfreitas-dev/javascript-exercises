var balance = 100;

console.log(balance += 50); // 150
console.log(balance -= 20); // 130
console.log(balance *= 2); // 260
console.log(balance /= 4); // 65
console.log(balance %= 7); // 2 

// The compound assignment operators (+=, -=, *=, /=, %=) perform an operation on the variable and then assign the result back to that variable. For example, balance += 50 is equivalent to balance = balance + 50.